        <h2 class="header">Institución</h2>
        <div>
            <p>
                Fundado en la ciudad de Rosario, el día 02 de octubre de 1920, en la calle 5 y B, del barrio Mendoza (como así lo destaca el Estatuto), el club Libertad está cumpliendo sus 97 años de vida.
            </p>
            <p>
                Surgía, por entonces, dentro de un barrio de gente laboriosa, de trabajadores de la Zona Oeste, como expresión de ideales y voluntades de sus fundadores. Buscando siempre el espíritu de unión y confraternidad, destacando lo más valorado por los hombres: la "Libertad" y por ello, desde sus comienzos, fomentar el deporte en general y toda clase de ejercicios. Desarrollando desde el inicio actividades sociales.
            </p>
            <p>
                "Libertad" nació por inspiración de un grupo de muchachos que amaban el deporte más arraigado en la barriada: el Foot Ball (como antaño se decía).
            </p>
            <p>
                Así se alquila en 1931 un terreno a $10.- mensuales para la cancha de fútbol en la manzana de Pedro Lino Funes-Rioja-Roullión-Córdoba. Por ese entonces, la secretaría deambulaba por distintos lugares, como la casa del Presidente de C.D. , por ese momento, Narciso del Felice, en calle 5 Nº 125 o alquilando un local en Mendoza 5050, luego al 5300, después funcionaría en los altos del ya desaparecido Cine Mendoza y luego, en Mendoza y Windels.
            </p>
            <p>
                Hacia el año 1932, el club contaba con 50 socios, 45 activos que realizaban las famosas Kermeses: en los años 1936, 37, 38 en los terrenos donde hoy está la Iglesia y la Esc. Ntra. Sra. de Pompeya y en el año 1939 se inauguraban las Kermeses en nuestro local.
            </p>
            <p>
                La década del 40 significó un crecimiento notable para el club, se adquirieron los primeros terrenos, se edificó la casa, se construyó la pista que primero fue de madera con escenario y luego de mosaicos. Cuentan los socios veteranos que todos colaboraron comprando 1 metro cuadrado de mosaicos para el piso del patio. Fue ese mismo patio, que ya hacia los 50, se llenó de prestigio realizando los "Bailes Populares", llegando hombres y mujeres de distintos lugares distantes de la ciudad, en los tranvías 13,14 y 20. Época de Orquestas Típicas y de Jazz, sobre el escenario (ubicado sobre un ángulo del patio con un piano del año 35) desfilaron figuras de la talla de Juan D' Arienzo, José Basso, Héctor Varela, Alberto Morán, Héctor y su Jazz, Varela Varelita, Jazz Casino de Sevilla, Estela Raval, y otros, es decir, lo mejor de esos tiempos, sin lugar a dudas.
            </p>
            <p>
                Entre otros eventos destacados estaban los Bailes de Carnaval, el "Baile de los Socios", donde se jugaba con papel picado, serpentinas, fuentones con agua llenos de globitos, con pomos de plomo, permitidos entonces. Se extendían hasta la madrugada y era impresionante la diversión.
            </p>
            <p>
                En cuanto a lo deportivo, para esa época, se realizaban torneos de tenis de mesa, bochas, tenis criollo, básquet, patín, ajedrez, box, torneos de truco, fútbol, juegos de salón y atletismo.
            </p>
            <p>
                Damas y caballeros formaron el grupo de "acrobacia y fantasía" de patín artístico, siendo su más ferviente colaborador el señor Benito Martínez.
            </p>
            <p>
                El "Básquetbol" es la actividad que movilizó y moviliza mayor cantidad de socios, simpatizantes y allegados. Al finalizar la década del 50 llegó al club un joven entusiasta y estudioso, que junto a un grupo de chicos, dieron al club, en pocos años grandes satisfacciones y campeonatos. Se logró el ascenso a primera división en el año 1965 y se obtuvieron los campeonatos de primera división en los años 1971 y 1973.
            </p>
            <p>
                Ya en los 60 el edificio del club creció mucho. Se construye la secretaría, ampliándose el salón, antes la secretaría y salón social. Con la incorporación de la magnífica pileta se llegó a contar con 2000 socios.
            </p>
            <p>
                De allí el modesto club avanzó vertiginosamente, con un caudal importante de socios , seguimos creciendo en todas las actividades deportivas. Se construyó en los 80 el Gimnasio sobre el salón, para satisfacer las distintas necesidades, pero sobre todo como requerimiento del básquet, ya que no se podía continuar practicando al aire libre. Se reformaron los baños y se hicieron nuevos vestuarios con gas natural y agua caliente. Ya se evidenciaba "otro nivel" para el "Libertad"
            </p>
            <p>
                En los años 90 se adquieren los terrenos en la calle Matienzo y se destinan a variados usos deportivos.
            </p>
            <p>
                La pileta cambió notablemente hacia el 2000 al concretarse un anhelo de todos: la "climatización" y así disfrutarla todo el año.
            </p>
            <p>
                Las ansias de progreso se evidenciaron este año 2008, al construir el piso de madera sobre el gimnasio, cumpliendo un sueño, que sólo se pudo lograr gracias a la colaboración y el trabajo arduo y desinteresado de algunos hombres que quieren realmente a nuestro club.
            </p>
            <p>
                Y así, hoy en día, la institución cuenta con las instalaciones necesarias para albergar confortablemente a casi 1000 socios, tratando de brindarles siempre lo mejor.
            </p>
            <p>
                Continuando con actividades sociales y deportivas como Gimnasia Rítmica y Deportiva, Karate, Patín, Natación, Folklore, Tenis con Paleta, Fútbol, Básquet y Voley (ya ascendidos ambos).
            </p>
            <p>
                Pero es importante destacar que todo lo detallado se ha logrado por hombres y mujeres que desde el pasado, vienen respondiendo, a pesar de distintas dificultades, a la inquietud de crecimiento.
            </p>
            <p>
                Conociendo nuestras bases y valorando nuestra historia, podemos proyectarnos en el presente. Por eso, El CLUB ATLÉTICO LIBERTAD no ha perdido su esencia, el respeto por los valores familiares y sociales, por la expresión y la unión, construyendo hacia el porvenir, para que todos disfruten y sientan al "LIBER" como propio.
            </p>
        </div>

